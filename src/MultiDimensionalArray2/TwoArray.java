package MultiDimensionalArray2;

public class TwoArray {
    public static void main(String[] args) {
        int[][] grid = new int[3][3];
        grid[0][0] = 10;
        grid[0][1] = 20;
        grid[0][2] = 30;
        System.out.println(grid[2][2]);
        System.out.print(grid[0][2]);
    }
}
